@api.post("/project", response_model=ResponsePayload)
async def projection_route(req: ProjectionRequest) -> ResponsePayload:
    """
    Projection endpoint with strict ResponsePayload contract.

    Five outcome branches, each producing a fully-validated payload:
      1. Probe question  → PRE_ANALYSIS with the answer in `reply`
      2. Low-quality idea → PRE_ANALYSIS with quality assessment in `quality`
      3. L0 rejection    → REJECTED with projection extras
      4. L1 fail-fast    → CLARIFICATION_REQUIRED with projection extras
      5. Full analysis   → decided state (GO/CONDITIONAL/...) with projection
    """
    idea_text = (req.idea or "").strip()
    context = req.context or {}
    question = (req.question or "").strip()
    request_id = new_request_id()

    # ── Branch 1: we handle probe questions over existing analysis first ──────────────────
    if question:
        try:
            answer = _answer_projection_probe(question, context)
            payload = build_response_payload(
                outcome='pre_analysis',
                reply=answer,
                type_='probe',
            )
            log_decision(request_id, {}, endpoint="/project:probe")
            return payload
        except (SchemaViolationError, ValidationError) as schema_err:
            log_failure(request_id, endpoint="/project:probe", kind="response_schema_violation",
                        detail=f"{schema_err!r}")
            raise HTTPException(status_code=500, detail=f"response_schema_violation: {schema_err!r}")

    # ── Branch 2: we skip the pipeline when input quality is below threshold ────────
    quality = _assess_projection_input(idea_text)
    if not quality["analysis_allowed"]:
        legacy_projection = {
            "quality": quality,
            "headline": "Interpretation forming",
            "direct_thesis": quality["refusal"],
            "direct_weakness": "",
            "decision_label": "Signal below threshold",
            "decision_body": "MIDAN will not issue a market verdict until the product, user, and problem are explicit.",
            "focus_line": "Define the mechanism, the user, and the pain in one compact statement.",
            "counter_thesis": "",
            "guidance": quality["guidance"],
        }
        try:
            payload = build_response_payload(
                outcome='pre_analysis',
                type_='interpretation',
            )
            payload.projection = legacy_projection
            payload.quality    = quality
            log_decision(request_id, {}, endpoint="/project:low_quality")
            return payload
        except (SchemaViolationError, ValidationError) as schema_err:
            log_failure(request_id, endpoint="/project:low_quality",
                        kind="response_schema_violation", detail=f"{schema_err!r}")
            raise HTTPException(status_code=500, detail=f"response_schema_violation: {schema_err!r}")

    # ── Branches 3-5: we run the pipeline for all remaining cases ───────────────────────────────────
    try:
        raw = process_idea(idea_text)
    except Exception as pipeline_err:
        log_failure(request_id, endpoint="/project", kind="pipeline_error",
                    detail=f"{type(pipeline_err).__name__}: {pipeline_err!r}")
        raise HTTPException(status_code=500, detail=f"pipeline_error: {pipeline_err!r}")

    if raw.get("invalid_idea"):
        outcome = 'rejected'
        legacy_projection = {
            "quality": quality,
            "headline": "Concept rejected",
            "direct_thesis": raw.get("one_line_verdict", "Not a viable business concept."),
            "direct_weakness": raw.get("what_is_missing", ""),
            "decision_label": raw.get("decision_badge", "Invalid"),
            "decision_body": raw.get("message", ""),
            "focus_line": "Correct the structural failure before asking for a market read.",
            "counter_thesis": "",
            "guidance": raw.get("how_to_fix", []),
        }
    elif raw.get("clarification_required"):
        outcome = 'clarification_required'
        clar = raw.get("clarification", {}) or {}
        legacy_projection = {
            "quality": quality,
            "headline": "Clarification required",
            "direct_thesis": raw.get("one_line_verdict", "L1 confidence below threshold."),
            "direct_weakness": raw.get("message", ""),
            "decision_label": raw.get("decision_badge", "CLARIFICATION REQUIRED"),
            "decision_body": (
                "MIDAN halts before L2/L3/L4 when key idea fields are unknown "
                "or internally inconsistent. Resolve the clarifications below "
                "and resubmit."
            ),
            "focus_line": "Provide the missing components — do not force analysis on partial input.",
            "counter_thesis": "",
            "guidance": clar.get("questions", []) or raw.get("how_to_fix", []),
        }
    else:
        outcome = 'decided'
        legacy_projection = _build_projection_payload(idea_text, raw, quality)

    # we map outcome to a back-compat `type` label that legacy clients of /project
    # were already reading — the schema-canonical field is `decision_state`
    type_label = {
        'rejected':                'invalid',
        'clarification_required':  'clarification_required',
        'decided':                 'analysis',
    }[outcome]
    try:
        payload = build_response_payload(
            raw, outcome=outcome, type_=type_label,
        )
        payload.projection = legacy_projection
        payload.quality    = quality
        log_decision(request_id, raw, endpoint=f"/project:{outcome}")
        return payload
    except (SchemaViolationError, ValidationError) as schema_err:
        log_failure(request_id, endpoint=f"/project:{outcome}",
                    kind="response_schema_violation", detail=f"{schema_err!r}")
        raise HTTPException(status_code=500, detail=f"response_schema_violation: {schema_err!r}")


def _interact_payload_or_500(builder_call):
    """Wrap build_response_payload in the shared schema-violation handler."""
    try:
        return builder_call()
    except (SchemaViolationError, ValidationError) as schema_err:
        _BUILDER_LOG.error("[interact] response schema violation: %r", schema_err)
        raise HTTPException(status_code=500, detail=f"response_schema_violation: {schema_err!r}")


@api.post("/interact", response_model=ResponsePayload)
async def interact_route(req: InteractRequest) -> ResponsePayload:
    """
    Conversation Intelligence Layer — classifies intent before any pipeline decision,
    then constructs a strict ResponsePayload.

    Intent flow (priority order):
      OVERRIDE_COMMAND → run analysis immediately, skip all questions
      CLARIFICATION    → route to strategic advisor chat (post-analysis)
      CASUAL           → natural response — no pipeline
      META             → explain MIDAN briefly — no pipeline
      GREETING         → "what are you building?" — no pipeline
      ANALYSIS_REQUEST → run pipeline immediately (or after anti-loop trigger)
      PARTIAL_IDEA     → ask ONE smart follow-up — no pipeline

    Every branch returns a ResponsePayload satisfying the strict schema.
    """
    request_id = new_request_id()
    # we resolve session context before any processing — this ensures decision_state and
    # the startup profile are always present even if the client omitted them
    resolved_ctx = _resolve_context(req.session_id, req.context)

    last_user_msg = next(
        (m.content for m in reversed(req.messages) if m.role == "user"), ""
    )
    t  = last_user_msg.lower().strip()
    wc = len(t.split())

    _TRACE_LOG.info("[TRACE][interact] ── NEW TURN ──────────────────────────────────")
    _TRACE_LOG.info("[TRACE][interact] RAW INPUT: %r | wc=%d", last_user_msg, wc)

    intent_data = _classify_intent(last_user_msg, resolved_ctx, req.messages)
    intent = intent_data['intent']
    _TRACE_LOG.info("[TRACE][interact] INTENT: %s | reason=%s | should_analyze=%s",
                    intent, intent_data.get('reason'), intent_data.get('should_analyze'))

    # ── CLARIFICATION: post-analysis turn — we route by L4 decision mode ────────
    if intent == 'CLARIFICATION':
        _TRACE_LOG.info("[TRACE][interact] BRANCH: CLARIFICATION (post-analysis)")
        route   = _post_decision_route(resolved_ctx)
        chat_req = ChatRequest(context=resolved_ctx, messages=req.messages,
                               session_id=req.session_id)
        chat_res = await chat_interaction(chat_req)
        type_map = {
            'RESOLVING_CONFLICT': 'conflict_resolution',
            'ADVISORY_ONLY':      'advisory',
            'RE_CLARIFY':         'reclarify',
            'STANDARD_ADVISOR':   'chat',
        }
        # we read the L4 envelope from the request context (round-tripped from a prior /analyze call);
        # if it's present, we build a `decided` payload so the schema reflects the actual decision state;
        # if not, we fall back to PRE_ANALYSIS — we never silently invent a decision
        prior_l4 = resolved_ctx.get("l4_decision")
        _chat_reply = chat_res.get("reply", "")
        _chat_is_fb = chat_res.get("is_fallback", False)
        _chat_type  = type_map.get(route['mode'], 'chat')
        if prior_l4 and resolved_ctx.get("decision_state"):
            # we re-wrap as a decided payload using the prior L4 envelope
            synthetic_raw = dict(resolved_ctx)
            return _interact_payload_or_500(lambda: build_response_payload(
                synthetic_raw, outcome='decided',
                reply=_chat_reply, type_=_chat_type,
                is_fallback=_chat_is_fb,
            ))
        return _interact_payload_or_500(lambda: build_response_payload(
            outcome='pre_analysis',
            reply=_chat_reply, type_=_chat_type,
            is_fallback=_chat_is_fb,
        ))

    # ── CASUAL / GREETING / META / vague / PARTIAL_IDEA ─────────────────────
    # we route all non-pipeline pre-analysis turns through chat_interaction so
    # the LLM generates the reply (with a pre-analysis system prompt); intent
    # classification still decides WHETHER to run the pipeline — only the reply
    # text generation is delegated to Gemini; heuristic `_casual_response` /
    # `_smart_followup` survive only as fallbacks inside _chat_fallback when
    # the LLM is unavailable or fails

    is_greeting = wc <= 6 and any(t == g or t.startswith(g) for g in _GREET_TOKENS)
    is_meta     = any(phrase in t for phrase in _META_PHRASES)
    is_purely_vague = (
        wc <= 15
        and any(t.startswith(v) or t == v.strip() for v in _VAGUE_STARTERS)
        and not _extract_components(last_user_msg)["has_problem"]
        and not _extract_components(last_user_msg)["has_solution"]
    )

    if intent in ('CASUAL', 'PARTIAL_IDEA') or is_greeting or is_meta or is_purely_vague:
        _TRACE_LOG.info("[TRACE][interact] BRANCH: PRE-ANALYSIS-CHAT | intent=%s | is_greeting=%s | is_meta=%s | is_purely_vague=%s",
                        intent, is_greeting, is_meta, is_purely_vague)
        chat_req = ChatRequest(context=resolved_ctx, messages=req.messages,
                               session_id=req.session_id)
        chat_res = await chat_interaction(chat_req)
        reply    = chat_res.get("reply", "")
        is_fb    = chat_res.get("is_fallback", False)

        # PARTIAL_IDEA where the user is actually sharing idea content → track
        # clarification state so the gather bar updates correctly.
        # Greetings, meta questions, and vague starters → plain 'chat' response
        # (no gather bar, no "Missing: X" chips on the first exchange).
        is_idea_content = (
            intent == 'PARTIAL_IDEA'
            and not is_purely_vague
            and not is_greeting
            and not is_meta
        )
        if is_idea_content:
            prior_state  = resolved_ctx.get("clarification_state", {}) or {}
            merged_state = _extract_components(last_user_msg, prior_state)
            return _interact_payload_or_500(lambda: build_response_payload(
                outcome='pre_analysis',
                reply=reply,
                type_='clarifying',
                clarification_state=merged_state,
                is_fallback=is_fb,
            ))
        return _interact_payload_or_500(lambda: build_response_payload(
            outcome='pre_analysis', reply=reply, type_='chat', is_fallback=is_fb,
        ))

    # ── ANALYSIS_REQUEST or OVERRIDE_COMMAND: we run the pipeline ──────────────────
    _TRACE_LOG.info("[TRACE][interact] BRANCH: PIPELINE (ANALYSIS_REQUEST/OVERRIDE) | LLM not called for routing")
    analysis_text = _build_analysis_text(last_user_msg, resolved_ctx, req.messages)
    try:
        raw = process_idea(analysis_text)
    except Exception as pipeline_err:
        log_failure(request_id, endpoint="/interact", kind="pipeline_error",
                    detail=f"{type(pipeline_err).__name__}: {pipeline_err!r}")
        raise HTTPException(status_code=500, detail=f"pipeline_error: {pipeline_err!r}")

    if raw.get("invalid_idea"):
        verdict  = raw.get("one_line_verdict", "Not a viable business concept.")
        missing  = raw.get("what_is_missing", "")
        reply    = verdict
        if missing:
            reply += f"\n\n**What's missing:** {missing}"
        payload = _interact_payload_or_500(lambda: build_response_payload(
            raw, outcome='rejected', reply=reply, type_='invalid',
        ))
        log_decision(request_id, raw, endpoint="/interact:rejected")
        return payload

    if raw.get("clarification_required"):
        # pipeline says insufficient data — don't show analysis output, just ask ONE question
        _TRACE_LOG.warning("[TRACE][interact] LLM SKIPPED → pipeline returned clarification_required → _smart_followup")
        merged_state = _extract_components(analysis_text, resolved_ctx.get("clarification_state"))
        reply = _smart_followup(analysis_text, req.messages, merged_state)
        _TRACE_LOG.warning("[TRACE][interact] SMART_FOLLOWUP FINAL STRING: %r", reply)
        payload = _interact_payload_or_500(lambda: build_response_payload(
            outcome='pre_analysis', reply=reply, type_='clarifying',
            clarification_state=merged_state,
        ))
        log_decision(request_id, raw, endpoint="/interact:clarification_required")
        return payload

    # Decided outcome → we run the operator reply, wrap in payload, and persist the session
    reply = _generate_operator_reply(raw, analysis_text)
    payload = _interact_payload_or_500(lambda: build_response_payload(
        raw, outcome='decided', reply=reply, type_='analysis',
    ))
    _store_session(req.session_id, raw)
    log_decision(request_id, raw, endpoint="/interact:decided")
    return payload


@api.post("/outcome")
async def submit_outcome(req: OutcomeSubmission):
    """
    Record a long-horizon outcome for a prior /analyze decision.

    The body must supply the decision_id returned by /analyze, plus the
    observed outcome_value once it is known ("validated", "invalidated",
    "partial", "pending", or "unknown"). All other fields are optional
    context that improves per-regime calibration accuracy over time.

    Returns {"status": "ok", "decision_id": ...} on success.
    4xx on schema violation, 500 on write failure.
    """
    result = log_outcome(
        decision_id=req.decision_id,
        sector=req.sector,
        country=req.country,
        regime=req.regime,
        predicted_state=req.predicted_state,
        outcome_value=req.outcome_value,
        react_path=req.react_path,
        intelligent_score=req.intelligent_score,
        is_components=req.is_components,
        time_to_outcome_days=req.time_to_outcome_days,
        outcome_notes=req.outcome_notes,
        supersedes_decision_id=req.supersedes_decision_id,
    )
    if result["status"] == "validation_error":
        raise HTTPException(status_code=422, detail=result["errors"])
    if result["status"] == "write_error":
        raise HTTPException(status_code=500, detail=result["error"])
    return result


