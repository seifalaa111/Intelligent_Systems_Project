# ── Server-side session store ────────────────────────────────────────────────
# we persist startup context here (profile + last L4 decision) across HTTP boundaries;
# we key by session_id — one UUID per conversation, generated by the frontend;
# on every chat/interact call we merge the stored context with the incoming context
# so the chat layer always has decision_state, l4_decision, and the startup profile —
# even if the client forgets to round-trip them
_SESSION_STORE: Dict[str, Dict[str, Any]] = {}
_SESSION_MAX = 500


def _build_session_context(raw: dict) -> dict:
    feat = raw.get("idea_features") or {}
    return {
        "idea":            raw.get("idea", ""),
        "sector":          raw.get("sector", ""),
        "country":         raw.get("country", ""),
        "regime":          raw.get("regime", ""),
        "tas_score":       raw.get("tas_score", 0),
        "signal_tier":     raw.get("signal_tier", ""),
        "dominant_risk":   raw.get("dominant_risk", ""),
        "decision_state":  raw.get("decision_state", ""),
        "decision_strength": raw.get("decision_strength") or {},
        "l4_decision":     raw.get("l4_decision") or {},
        "idea_features":   feat,
        "business_model":  feat.get("business_model", ""),
        "target_segment":  feat.get("target_segment", ""),
        "stage":           feat.get("stage", ""),
        "industry":        raw.get("sector", ""),
        "region":          raw.get("country", ""),
        "strategic_interpretation": raw.get("strategic_interpretation", ""),
        "key_driver":      raw.get("key_driver", ""),
        "main_risk":       raw.get("main_risk", ""),
        "l3_reasoning":    raw.get("l3_reasoning") or {},
    }


def _store_session(session_id: Optional[str], raw: dict) -> None:
    if not session_id:
        return
    if len(_SESSION_STORE) >= _SESSION_MAX:
        del _SESSION_STORE[next(iter(_SESSION_STORE))]
    _SESSION_STORE[session_id] = _build_session_context(raw)


def _resolve_context(session_id: Optional[str], incoming: dict) -> dict:
    """Merge stored session context with incoming context. Incoming wins on conflict."""
    if not session_id:
        return incoming
    stored = _SESSION_STORE.get(session_id)
    if not stored:
        return incoming
    return {**stored, **incoming}


@api.post("/analyze", response_model=ResponsePayload)
async def analyze_idea(req: IdeaRequest) -> ResponsePayload:
    """
    Run the full pipeline and return a strict ResponsePayload.

    Schema is enforced by `response_model=ResponsePayload`. Any field-shape
    drift inside `build_response_payload` raises ValidationError, which
    surfaces as a 500 with explicit detail — never silent auto-correction.
    """
    request_id = new_request_id()
    if not MODELS_LOADED:
        log_failure(request_id, endpoint="/analyze", kind="models_not_loaded",
                    detail="MODELS_LOADED is False at endpoint entry")
        raise HTTPException(status_code=500, detail="Models failed to load.")
    sector_key   = SECTOR_LABEL_MAP.get(req.sector, "fintech")
    country_code = req.country.split(" — ")[0]
    if not req.idea or len(req.idea.strip()) < 5:
        log_failure(request_id, endpoint="/analyze", kind="idea_too_short",
                    detail=f"len={len(req.idea or '')}")
        raise HTTPException(status_code=400, detail="Idea too short")
    try:
        raw = process_idea(req.idea, sector_key, country_code)
    except Exception as pipeline_err:
        log_failure(request_id, endpoint="/analyze", kind="pipeline_error",
                    detail=f"{type(pipeline_err).__name__}: {pipeline_err!r}")
        raise HTTPException(status_code=500, detail=f"pipeline_error: {pipeline_err!r}")

    # we map raw outcome to a strict payload here; we determine outcome explicitly —
    # NOT by auto-detection inside build_response_payload
    if raw.get("invalid_idea"):
        outcome = 'rejected'
    elif raw.get("clarification_required"):
        outcome = 'clarification_required'
    else:
        outcome = 'decided'
    try:
        payload = build_response_payload(raw, outcome=outcome)
    except (SchemaViolationError, ValidationError) as schema_err:
        log_failure(request_id, endpoint="/analyze", kind="response_schema_violation",
                    detail=f"{schema_err!r} — raw outcome={outcome}")
        raise HTTPException(status_code=500, detail=f"response_schema_violation: {schema_err!r}")

    _store_session(req.session_id, raw)
    log_decision(request_id, raw, endpoint="/analyze")
    return payload


# ── CHAT FALLBACK (no LLM) — L4-grounded ─────────────────────────────────────
# we build every reply from the L4 decision envelope, NOT from legacy TAS/tier fields;
# we branch by post-decision conversation mode (RESOLVING_CONFLICT, ADVISORY_ONLY,
# RE_CLARIFY, STANDARD_ADVISOR); when the prior decision is missing or invalid,
# we refuse to advise and re-prompt for the idea

@api.post("/chat")
async def chat_interaction(req: ChatRequest):
    """
    Single chat entry point. All chat replies (including those routed in from
    /interact) flow through here.

    Behavior contract:
      1. Gemini is used for LLM replies (unified system prompt built by
         _chat_llm_reply, mode-aware, with strict L4 input contract).
      2. Hard L4 guard inside _chat_llm_reply: if no decision_state in context,
         it returns a consultative pre-analysis reply WITHOUT calling the LLM
         (never sends empty L4 fields to the model).
      3. On any LLM API failure → sanitized _chat_fallback (never raw output).
      4. Every returned reply is run through _sanitize_chat_output as a final
         defence-in-depth strip of internal labels, layer names, debug stamps,
         and UNKNOWN leakage.
    """
    # we merge session context here so the chat layer always has decision_state + startup profile
    resolved_ctx = _resolve_context(getattr(req, 'session_id', None), req.context)
    if resolved_ctx is not req.context:
        req = ChatRequest(context=resolved_ctx, messages=req.messages,
                          session_id=getattr(req, 'session_id', None))
    decision_state = resolved_ctx.get('decision_state') or (resolved_ctx.get('l4_decision') or {}).get('decision_state')

    # ── Pre-analysis: Gemini owns every reply via _chat_pre_analysis_reply.
    # That function always returns a string — the error fallback is baked in.
    # No heuristic intercept exists here.
    if not decision_state:
        _TRACE_LOG.info("[TRACE][chat_interaction] PRE-ANALYSIS → _chat_pre_analysis_reply")
        reply = _chat_pre_analysis_reply(req)
        is_fallback = (reply == _PRE_ANALYSIS_ERROR_FALLBACK)
        _TRACE_LOG.info("[TRACE][chat_interaction] reply=%r | is_fallback=%s", reply[:120], is_fallback)
        return {"success": True, "reply": _sanitize_chat_output(reply), "is_fallback": is_fallback}

    # ── Post-analysis: LLM with full decision context, _chat_fallback on failure
    try:
        _TRACE_LOG.info("[TRACE][chat_interaction] POST-ANALYSIS → _chat_llm_reply")
        reply = _chat_llm_reply(req)
        if reply:
            _TRACE_LOG.info("[TRACE][chat_interaction] LLM CALLED (post) — reply=%r", reply[:120])
            return {"success": True, "reply": _sanitize_chat_output(reply), "is_fallback": False}
        __import__('logging').getLogger("midan.chat").warning("[CHAT] post-analysis LLM returned None → _chat_fallback")
        _TRACE_LOG.warning("[TRACE][chat_interaction] LLM SKIPPED (post, None) → _chat_fallback")
        fb = _chat_fallback(req)
        _TRACE_LOG.warning("[TRACE][chat_interaction] POST FALLBACK: %r", fb[:120])
        return {"success": True, "reply": _sanitize_chat_output(fb), "is_fallback": True}
    except Exception as llm_err:
        __import__('logging').getLogger("midan.chat").warning(
            "[CHAT] post-analysis LLM failed (%s: %r) → _chat_fallback", type(llm_err).__name__, llm_err)
        _TRACE_LOG.warning("[TRACE][chat_interaction] LLM SKIPPED (post, exception) → _chat_fallback")
        fb = _chat_fallback(req)
        _TRACE_LOG.warning("[TRACE][chat_interaction] POST FALLBACK: %r", fb[:120])
        return {"success": True, "reply": _sanitize_chat_output(fb), "is_fallback": True}


